#!/usr/bin/env perl

use strict;
use warnings;


# Strings (Cadenas de texto)
my $string = "hola mundo marcos";

print ($string . "\n");


#Integer (Enteros)
my $entero = 188;
print("Tipo de dato entero: $entero \n");


#Float (Decimales)
my $decimal = 8.33;
print ("Tipo de dato decimal: $decimal \n");

#Octal (0,...,7)
my $octal = 044556677;
print("Tipo de dato octal: $octal " . "\n");


#hexadecimal (0,...,9, A,...,F)
my $hexadecimal = 0xA;
print("Tipo de dato hexadecimal: $hexadecimal" . "\n");

